package com.java.domain;

public class CouponVO {
	private String couponName;
	private String couponID;
	private String couponStartDate;
	private String couponExpDate;
	private String couponSaleRate;
	private String couponContent;
	public String getCouponName() {
		return couponName;
	}
	public void setCouponName(String couponName) {
		this.couponName = couponName;
	}
	public String getCouponID() {
		return couponID;
	}
	public void setCouponID(String couponID) {
		this.couponID = couponID;
	}
	public String getCouponStartDate() {
		return couponStartDate;
	}
	public void setCouponStartDate(String couponStartDate) {
		this.couponStartDate = couponStartDate;
	}
	public String getCouponExpDate() {
		return couponExpDate;
	}
	public void setCouponExpDate(String couponExpDate) {
		this.couponExpDate = couponExpDate;
	}
	public String getCouponSaleRate() {
		return couponSaleRate;
	}
	public void setCouponSaleRate(String couponSaleRate) {
		this.couponSaleRate = couponSaleRate;
	}
	public String getCouponContent() {
		return couponContent;
	}
	public void setCouponContent(String couponContent) {
		this.couponContent = couponContent;
	}
	
	
}
